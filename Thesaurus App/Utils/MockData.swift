import Foundation




public func makeWords(_ count: Int, size: Int = 5) -> [String] {
    let mockLetters = "qwertyuiopasdfghjklzxcvbnm".sorted(by: >)
    var words = [String]()
    for c in 1...count {
        var word: String = ""
        for _ in 1...size {
            word += String(mockLetters[Int.random(in: 1...mockLetters.count - 1)])
        }
        word += "\(c)"
        words.append(word)
    }
    return words
}

